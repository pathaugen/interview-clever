# ---------- ---------- ---------- ---------- ---------- ---------- ---------- ---------- ---------- ---------- #
def get_css( file_name ):
    css = ''
    
    
    # ---------- ---------- ---------- ---------- ---------- #
    if file_name == '': css += open('resources/css/framework.css').read()
    else:
        # ---------- ---------- ---------- ---------- ---------- #
        # Load requested CSS file
        css += open('resources/css/'+file_name).read()
        # ---------- ---------- ---------- ---------- ---------- #
    # ---------- ---------- ---------- ---------- ---------- #
    
    
    return css
# ---------- ---------- ---------- ---------- ---------- ---------- ---------- ---------- ---------- ---------- #

